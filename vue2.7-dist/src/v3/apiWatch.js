import { isRef } from './reactivity/ref.js';
import { isShallow, isReactive } from './reactivity/reactive.js';
import { isArray, isFunction, noop, hasChanged, emptyObject } from '../shared/util.js';
import { isServerRendering } from '../core/util/env.js';
import '../core/util/options.js';
import { warn } from '../core/util/debug.js';
import '../core/observer/dep.js';
import '../core/observer/array.js';
import Watcher from '../core/observer/watcher.js';
import { currentInstance } from './currentInstance.js';
import { traverse } from '../core/observer/traverse.js';
import { queueWatcher } from '../core/observer/scheduler.js';
import '../core/instance/proxy.js';
import '../core/util/perf.js';
